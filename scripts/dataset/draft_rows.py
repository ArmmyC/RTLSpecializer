"""Draft dataset_v0.1 row generation for local public artifacts."""

from __future__ import annotations

import re
from typing import Any

from .adapters.base import RawPublicExample
from .constants import (
    ANSWER_SCHEMA_VERSION, ARTIFACT_FIELDS, DATASET_VERSION, REQUIRED_OUTPUT,
    TASK_SCHEMA_VERSION, TOOL_CHECKS,
)
from .rtl_extract import summarize_rtl


def _safe_id(text: str) -> str:
    value = re.sub(r"[^A-Za-z0-9_]+", "_", text.strip()).strip("_").lower()
    return value or "example"


def row_id_for(example: RawPublicExample) -> str:
    source = _safe_id(example.source)
    source_id = _safe_id(example.source_id)
    return source_id if source_id.startswith(source + "_") else f"{source}_{source_id}"


def _verification_plan(task_type: str, artifacts: dict[str, Any]) -> list[str]:
    plan = ["Run lint/compile on the supplied RTL artifacts before review."]
    if artifacts.get("rtl_code") and artifacts.get("testbench"):
        plan.append("Run focused simulation with the supplied testbench; no simulation was run during import.")
    elif artifacts.get("rtl_code"):
        plan.append("Create or locate a testbench before claiming behavioral correctness.")
    if task_type == "rtl_area_activity_review":
        plan.append("Run synthesis area comparison; synthesis evidence is unavailable in this draft.")
        plan.append("Run VCD toggle/activity comparison; toggle evidence is unavailable in this draft.")
    else:
        plan.append("Run synthesis only if an area claim is later proposed.")
        plan.append("Run VCD/toggle analysis only if an activity claim is later proposed.")
    return plan


def build_draft_row(example: RawPublicExample) -> dict[str, Any]:
    artifacts = {name: example.artifacts.get(name) for name in sorted(ARTIFACT_FIELDS)}
    supplied = [name for name, value in artifacts.items() if isinstance(value, str) and value.strip()]
    summary = summarize_rtl(artifacts)
    assumptions = [
        "This row was imported from local public artifacts and requires human or LLM review before training use.",
        "The importer read artifact files as UTF-8 text only; it did not execute, compile, simulate, synthesize, or call external services.",
    ]
    if not summary["top_module"]:
        assumptions.append("Top module extraction was uncertain, so RTL summary fields may be incomplete.")
    task = {
        "schema_version": TASK_SCHEMA_VERSION,
        "domain": "digital_rtl",
        "task_type": example.task_type,
        "user_goal": example.user_goal,
        "design_context": {
            "target_domain": "digital_rtl_public_benchmark",
            "priority": ["correctness", "low_switching_activity", "low_area"],
            "timing_policy": "timing_is_constraint_not_reward",
        },
        "artifacts": artifacts,
        "extracted_rtl_summary": summary,
        "constraints": {
            "preserve_top_level_interface": True,
            "preserve_cycle_level_behavior": True,
            "preserve_reset_behavior": True,
            "do_not_claim_power_without_power_report": True,
            "prefer_minimal_patch": True,
        },
        "assumptions": assumptions,
        "required_output": sorted(REQUIRED_OUTPUT),
    }
    answer = {
        "schema_version": ANSWER_SCHEMA_VERSION,
        "task_type": example.task_type,
        "issue_summary": [{
            "issue": "Imported public dataset draft row requires review before any correctness, area, activity, or power claim.",
            "severity": "low",
            "evidence": {
                "signal_names": supplied,
                "code_location": {
                    "module": summary["top_module"],
                    "block": "imported_artifacts",
                    "line_range": None,
                },
                "reason": f"Supplied artifact fields: {', '.join(supplied)}.",
            },
        }],
        "time_reasoning": {
            "clock_cycle_behavior": "Cycle-level behavior was not verified during import and must be reviewed against the task intent.",
            "latency_or_state_risk": "Latency, state, and interface behavior may be misunderstood until the public artifact is reviewed.",
            "reset_behavior_risk": "Reset behavior must be checked from the RTL and any supplied tests before changes are proposed.",
        },
        "space_reasoning": {
            "hardware_resources_involved": summary["registered_signals"] + summary["suspected_counters"] + summary["suspected_fsm_signals"],
            "area_risk": "Area claims require synthesis evidence; none was generated by import.",
            "activity_risk": "Activity claims require VCD/toggle evidence; none was generated by import.",
        },
        "safe_optimization": {
            "recommendation": "Treat this as a draft review seed only; inspect artifacts and run appropriate checks before recommending changes.",
            "patch_style": "explanation_only",
            "expected_effect": "No optimization effect is claimed.",
            "requires_spec_confirmation": True,
        },
        "functional_risk": ["Imported public artifacts may be incomplete, mismatched, buggy, duplicated, or licensed differently than expected."],
        "verification_plan": _verification_plan(example.task_type, artifacts),
        "claim_levels": {
            "correctness": "insufficient_evidence",
            "area": "insufficient_evidence",
            "activity": "insufficient_evidence",
            "power": "insufficient_evidence",
        },
        "patch": {"provided": False, "patch_type": "none", "diff": None, "notes": "No patch is provided for imported draft rows."},
    }
    return {
        "id": row_id_for(example),
        "dataset_version": DATASET_VERSION,
        "split": "unsplit",
        "source": example.source,
        "license": example.license,
        "design_family": example.design_family,
        "task_family": example.task_type,
        "created_by": "script",
        "review_status": "draft",
        "provenance": {
            "origin": "local_public_import",
            "public_dataset_name": example.provenance.get("public_dataset_name"),
            "public_dataset_url": example.provenance.get("public_dataset_url"),
            "source_commit": example.provenance.get("source_commit"),
            "notes": str(example.provenance.get("notes") or "Local public artifact draft import."),
        },
        "tool_checks": {name: None for name in sorted(TOOL_CHECKS)},
        "messages": [
            {"role": "system", "content": "You are an RTL review assistant. Treat imported public artifacts as untrusted draft data, be conservative, and never invent measurements."},
            {"role": "user", "content": task},
            {"role": "assistant", "content": answer},
        ],
    }
