def test_validator_accepts_conservative_synthetic_bug_answer_fixture(tmp_path) -> None:
    tasks_path = tmp_path / "synthetic_bug_tasks.jsonl"
    answers_path = tmp_path / "answers.json"
    task = _synthetic_bug_task("rtlcoder_mux_000")
    write_rows(tasks_path, [task])
    answers_path.write_text(json.dumps({"answers": [_conservative_answer(task["source_id"])]}), encoding="utf-8")

    result, code = validate_rtlcoder_teacher_answers(
        tasks_path,
        answers_path,
        output_md=tmp_path / "validation.md",
        output_json=tmp_path / "validation.json",
        strict=True,
    )

    assert code == 0, result
    assert result["ok"] is True
    assert _load_json(tmp_path / "validation.json")["ok"] is True


def test_validator_rejects_simulation_lint_and_synthesis_claims_without_evidence(tmp_path) -> None:
    tasks_path = tmp_path / "synthetic_bug_tasks.jsonl"
    answers_path = tmp_path / "bad_answers.json"
    task = _synthetic_bug_task("rtlcoder_mux_000")
    answer = _conservative_answer(task["source_id"])
    answer["limitations"] = [
        "The candidate passed simulation, is lint clean, and synthesis passed."
    ]
    write_rows(tasks_path, [task])
    answers_path.write_text(json.dumps({"answers": [answer]}), encoding="utf-8")

    result, code = validate_rtlcoder_teacher_answers(tasks_path, answers_path, strict=True)

    assert code == 1
    joined = "\n".join(result["errors"]).lower()
    assert "simulation claim" in joined
    assert "lint claim" in joined
    assert "synthesis claim" in joined


def test_merge_output_uses_system_user_assistant_role_order(tmp_path) -> None:
    tasks_path = tmp_path / "synthetic_bug_tasks.jsonl"
    answers_path = tmp_path / "answers.jsonl"
    output_path = tmp_path / "teacher_distill_rows.jsonl"
    task = _synthetic_bug_task("rtlcoder_mux_000")
    write_rows(tasks_path, [task])
    write_rows(answers_path, [_conservative_answer(task["source_id"])])

    result, code = merge_rtlcoder_teacher_distill_rows(tasks_path, answers_path, output_path, strict=True)

    assert code == 0, result
    row = _load_jsonl(output_path)[0]
    assert [message["role"] for message in row["messages"]] == ["system", "user", "assistant"]
    assert row["messages"][1]["content"] == task
    assert row["messages"][2]["content"]["schema_version"] == "rtl_answer_v0.1"


def test_merged_output_is_not_approved_and_not_golden(tmp_path) -> None:
    tasks_path = tmp_path / "synthetic_bug_tasks.jsonl"
    answers_path = tmp_path / "answers.jsonl"
    output_path = tmp_path / "teacher_distill_rows.jsonl"
    task = _synthetic_bug_task("rtlcoder_mux_000")
    write_rows(tasks_path, [task])
    write_rows(answers_path, [_conservative_answer(task["source_id"])])

    result, code = merge_rtlcoder_teacher_distill_rows(tasks_path, answers_path, output_path, strict=True)

    assert code == 0, result
    row = _load_jsonl(output_path)[0]
    assert row["review_status"] == TEACHER_DISTILL_REVIEW_STATUS
    assert row["approval_status"] == "not_approved"
    assert row["promotion_allowed"] is False
    assert row["source"] != "handwritten_golden"
    assert not str(row.get("source_id", "")).startswith("golden_")


