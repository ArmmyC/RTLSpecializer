from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

from scripts.dataset.import_rtlcoder_dataset import import_rtlcoder_dataset


ROOT = Path(__file__).resolve().parents[2]
FIXTURE = ROOT / "tests" / "fixtures" / "rtlcoder" / "dataset" / "Resyn27k.json"


def _load_jsonl(path: Path) -> list[dict]:
    return [json.loads(line) for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]


def test_import_builds_raw_index_and_reports(tmp_path) -> None:
    output_index = tmp_path / "raw_index.jsonl"
    output_md = tmp_path / "report.md"
    output_json = tmp_path / "report.json"
    result, code = import_rtlcoder_dataset(FIXTURE, output_index, output_md, output_json)
    assert code == 0, result
    assert result["rows_total"] == 3
    assert result["imported_rows"] == 3
    assert result["instruction_field"] == "Instruction"
    assert result["rtl_field"] == "Response"
    rows = _load_jsonl(output_index)
    assert rows[0]["source_id"] == "rtlcoder_resyn27k_000001"
    assert rows[0]["provenance"] == "external_rtlcoder_gpt_generated_unverified"
    assert rows[0]["detected_module_names"] == ["counter8"]
    assert rows[0]["rough_design_family"] == "counter"
    assert rows[1]["detected_module_names"] == ["helper_decoder", "ram_wrapper"]
    assert any("Multiple module declarations detected" in warning for warning in rows[1]["import_warnings"])
    assert any("No module declarations were detected" in warning for warning in rows[2]["import_warnings"])
    report = json.loads(output_json.read_text(encoding="utf-8"))
    assert report["import_summary"]["rows_with_multiple_modules"] == ["rtlcoder_resyn27k_000002"]
    assert "generated by GPT-3.5-turbo" in " ".join(report["schema_inspection"]["upstream"]["provenance_notes"])
    assert output_md.read_text(encoding="utf-8").startswith("<!-- created_by: import_rtlcoder_dataset -->")


def test_limit_and_start_index_window_rows(tmp_path) -> None:
    output_index = tmp_path / "raw_index.jsonl"
    result, code = import_rtlcoder_dataset(
        FIXTURE,
        output_index,
        tmp_path / "report.md",
        tmp_path / "report.json",
        limit=1,
        start_index=1,
    )
    assert code == 0, result
    rows = _load_jsonl(output_index)
    assert len(rows) == 1
    assert rows[0]["source_record_index"] == 1
    assert rows[0]["source_id"] == "rtlcoder_resyn27k_000002"


def test_force_requires_managed_outputs(tmp_path) -> None:
    output_index = tmp_path / "raw_index.jsonl"
    output_index.write_text("not managed\n", encoding="utf-8")
    result, code = import_rtlcoder_dataset(
        FIXTURE,
        output_index,
        tmp_path / "report.md",
        tmp_path / "report.json",
        force=True,
    )
    assert code == 1
    assert "not a managed RTLCoder raw index" in " ".join(result["errors"])


def test_force_replaces_managed_outputs(tmp_path) -> None:
    output_index = tmp_path / "raw_index.jsonl"
    output_md = tmp_path / "report.md"
    output_json = tmp_path / "report.json"
    first, first_code = import_rtlcoder_dataset(FIXTURE, output_index, output_md, output_json)
    assert first_code == 0, first
    second, second_code = import_rtlcoder_dataset(
        FIXTURE,
        output_index,
        output_md,
        output_json,
        limit=1,
        start_index=1,
        force=True,
    )
    assert second_code == 0, second
    rows = _load_jsonl(output_index)
    assert len(rows) == 1
    assert rows[0]["source_id"] == "rtlcoder_resyn27k_000002"


def test_cli_json_output_is_parseable(tmp_path) -> None:
    completed = subprocess.run(
        [
            sys.executable,
            "scripts/dataset/import_rtlcoder_dataset.py",
            "--input",
            str(FIXTURE),
            "--output-index",
            str(tmp_path / "raw_index.jsonl"),
            "--output-report-md",
            str(tmp_path / "report.md"),
            "--output-report-json",
            str(tmp_path / "report.json"),
            "--limit",
            "1",
            "--json",
        ],
        cwd=ROOT,
        text=True,
        capture_output=True,
        check=False,
    )
    assert completed.returncode == 0, completed.stderr
    payload = json.loads(completed.stdout)
    assert payload["ok"] is True
    assert payload["imported_rows"] == 1
